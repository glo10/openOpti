package fr.cnam.openopti.beans;

public class Oam {
	protected int id_t;
	protected String nom_t;
	protected String adresse_t;
	protected String ville_t;
	protected String cp_t;
	protected String tel_t;
	protected String email_t;
	protected String fax_t;
	
	
	public Oam() {
		super();
	}
	
	public int getId_t() {
		return id_t;
	}
	
	public void setId_t(int id_t) {
		this.id_t = id_t;
	}
	
	public String getNom_t() {
		return nom_t;
	}
	
	public void setNom_t(String nom_t) {
		this.nom_t = nom_t;
	}
	
	public String getAdresse_t() {
		return adresse_t;
	}
	
	public void setAdresse_t(String adresse_t) {
		this.adresse_t = adresse_t;
	}
	
	public String getVille_t() {
		return ville_t;
	}
	
	public void setVille_t(String ville_t) {
		this.ville_t = ville_t;
	}
	
	public String getCp_t() {
		return cp_t;
	}
	
	public void setCp_t(String cp_t) {
		this.cp_t = cp_t;
	}
	
	public String getTel_t() {
		return tel_t;
	}
	
	public void setTel_t(String tel_t) {
		this.tel_t = tel_t;
	}
	
	public String getEmail_t() {
		return email_t;
	}
	
	public void setEmail_t(String email_t) {
		this.email_t = email_t;
	}
	
	public String getFax_t() {
		return fax_t;
	}
	
	public void setFax_t(String fax_t) {
		this.fax_t = fax_t;
	}
}
